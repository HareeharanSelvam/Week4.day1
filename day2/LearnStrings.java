package week3.day2;

public class LearnStrings {

	public static void main(String[] args) {
		String s1 = "Java"; // String declared by literal
		char ch[] = {'S','t','r','i','n','g'}; // char array 
		String s2 = new String(ch); // converting char array into String
		String s3 = new String("Dinga"); // creating java string by new keyword or string instance
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);
	}

}
